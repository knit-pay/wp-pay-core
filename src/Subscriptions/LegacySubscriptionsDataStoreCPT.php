namespace Pronamic\WordPress\Pay\Subscriptions;

use Pronamic\WordPress\Pay\Payments\LegacyPaymentsDataStoreCPT;

/**
 * Legacy subscriptions data store CPT
 *
 * @link https://woocommerce.com/2017/04/woocommerce-3-0-release/
 * @link https://woocommerce.wordpress.com/2016/10/27/the-new-crud-classes-in-woocommerce-2-7/
 * @author  Remco Tolsma
 * @version 2.1.0
 * @since   2.0.1
 */
class LegacySubscriptionsDataStoreCPT extends LegacyPaymentsDataStoreCPT {
	/**
	 * Read post meta.
	 *
	 * @param Subscription $subscription The subscription to read.
	 */
	protected function read_post_meta( $subscription ) {
		// Next payment date.
		if ( null === $subscription->next_payment_date ) {
			$subscription->next_payment_date = $this->get_meta_date( $subscription->get_id(), 'next_payment' );
		}
	}
}
